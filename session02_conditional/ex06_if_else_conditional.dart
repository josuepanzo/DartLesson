void main(List<String> args) {
  // IF ELSE CONDITIONAL
  int note = 3;

  // FIRST EXAMPLE
  if (note <= 5) {
    print("Reprovado");
  }

  // SECOND EXAMPLE
  if (note >= 10) {
    print("Reprovado");
  } else {
    print("Reprovado");
  }

  // THIRD EXAMPLE
  if (note >= 10 && note <= 20) {
    print("Aprovado");
  } else if (note >= 6 && note <= 9) {
    print("Recurso");
  } else if (note <= 5) {
    print("Reprovado");
  }
}
